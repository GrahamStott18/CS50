// Abstraction with parameterization

// Tells compiler to load specified library(s) into program
#include <cs50.h>
#include <stdio.h>

void meow(int n);

int main(void)
{
    meow(3);
}

// Meow some number of times
void meow(int n)
{
    for (int i = 0; i < n; i++)
    {
        printf("meow\n");
    }
}